function somar(num1, num2){
    let resultado;

    resultado = num1 + num2;
    return resultado
}
function dividir(num1, num2){
    let resultado;

    resultado = num1 / num2;
    return resultado
}
function subtrair(num1, num2){
    let resultado;

    resultado = num1 - num2;
    return resultado
}
function multiplicacao(num1, num2){
    let resultado;

    resultado = num1 - num2;
    return resultado
}
